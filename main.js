// Modal elements
const modal = document.getElementById('popupModal');
const popupTitle = document.getElementById('popupTitle');
const popupBody = document.getElementById('popupBody');
const closeBtn = document.querySelector('.popup-close');
const overlay = document.querySelector('.popup-overlay');

function showPopup(id) {
    const data = popupData[id];
    if (data) {
        popupTitle.innerHTML = `<i class="fas fa-info-circle"></i> ${data.title}`;
        popupBody.innerHTML = data.content;
        modal.classList.add('show');
        document.body.style.overflow = 'hidden';
    }
}

function closePopup() {
    modal.classList.remove('show');
    document.body.style.overflow = '';
}

document.querySelectorAll('[data-popup]').forEach(element => {
    element.addEventListener('click', (e) => {
        e.stopPropagation();
        showPopup(element.getAttribute('data-popup'));
    });
});

if (closeBtn) closeBtn.addEventListener('click', closePopup);
if (overlay) overlay.addEventListener('click', closePopup);

document.addEventListener('keydown', (e) => {
    if (e.key === 'Escape' && modal.classList.contains('show')) {
        closePopup();
    }
});

// Touch animations
const clickableItems = document.querySelectorAll('.member-chip, .week-card, .lamp-item, .pancasila-item');
clickableItems.forEach(el => {
    el.addEventListener('touchstart', function() {
        this.style.transform = 'scale(0.98)';
        this.style.transition = 'transform 0.1s';
    });
    el.addEventListener('touchend', function() {
        this.style.transform = 'scale(1)';
    });
    el.addEventListener('mousedown', function() {
        this.style.transform = 'scale(0.98)';
    });
    el.addEventListener('mouseup', function() {
        this.style.transform = 'scale(1)';
    });
    el.addEventListener('mouseleave', function() {
        this.style.transform = 'scale(1)';
    });
});

// Scroll reveal
const cards = document.querySelectorAll('.glass-card');
const observer = new IntersectionObserver((entries) => {
    entries.forEach(entry => {
        if (entry.isIntersecting) {
            entry.target.style.opacity = '1';
            entry.target.style.transform = 'translateY(0px)';
        }
    });
}, { threshold: 0.1, rootMargin: "0px 0px -20px 0px" });

cards.forEach(card => {
    card.style.opacity = '0';
    card.style.transform = 'translateY(18px)';
    card.style.transition = 'opacity 0.5s cubic-bezier(0.2, 0.9, 0.4, 1.1), transform 0.5s ease';
    observer.observe(card);
});

